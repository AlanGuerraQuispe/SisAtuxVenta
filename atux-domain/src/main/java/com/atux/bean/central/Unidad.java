public class Unidad {
    private String coUnidad;
    private String deUnidad;
    private String deUnidadCorta;

    public String getCoUnidad() {
        return coUnidad;
    }

    public void setCoUnidad(String coUnidad) {
        this.coUnidad = coUnidad;
    }

    public String getDeUnidad() {
        return deUnidad;
    }

    public void setDeUnidad(String deUnidad) {
        this.deUnidad = deUnidad;
    }

    public String getDeUnidadCorta() {
        return deUnidadCorta;
    }

    public void setDeUnidadCorta(String deUnidadCorta) {
        this.deUnidadCorta = deUnidadCorta;
    }
}
