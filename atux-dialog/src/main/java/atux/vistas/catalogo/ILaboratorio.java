    private boolean guardarActualizar() throws SQLException{
        this.txtCodigo.setText(this.txtCodigo.getText().toUpperCase());
        this.txtAbreviatura.setText(this.txtAbreviatura.getText().toUpperCase());
        this.txtLaboratorio.setText(this.txtLaboratorio.getText().toUpperCase());
        this.txtCiudad.setText(this.txtCiudad.getText().toUpperCase());
        this.txtLaboratorio.setText(this.txtLaboratorio.getText().toUpperCase());
        this.txtDireccion.setText(this.txtDireccion.getText().toUpperCase());
        this.txtEmailLaboratorio.setText(this.txtEmailLaboratorio.getText().toUpperCase());
        this.txtEmailContacto.setText(this.txtEmailContacto.getText().toUpperCase());
        this.txtFaxLaboratorio.setText(this.txtFaxLaboratorio.getText().toUpperCase());
        this.txtTelefContacto.setText(this.txtTelefContacto.getText().toUpperCase());
        this.txtMovilContacto.setText(this.txtMovilContacto.getText().toUpperCase());
        this.txtTelefLaboratorio.setText(this.txtTelefLaboratorio.getText().toUpperCase());

        cp.getLaboratorio().setCoCompania(AtuxVariables.vCodigoCompania);
        cp.getLaboratorio().setCoLaboratorio(txtCodigo.getText());
        cp.getLaboratorio().setDeAbrevLab(txtAbreviatura.getText());
        cp.getLaboratorio().setDeLaboratorio(this.txtLaboratorio.getText());
        cp.getLaboratorio().setDeDirecLaboratorio(this.txtDireccion.getText());
        cp.getLaboratorio().setDeCiudad(this.txtCiudad.getText());
        cp.getLaboratorio().setNoEmailLab(this.txtEmailLaboratorio.getText());
        cp.getLaboratorio().setNuTelefLaboratorio(this.txtTelefLaboratorio.getText());
        cp.getLaboratorio().setNuFaxLaboratorio(this.txtFaxLaboratorio.getText());
        cp.getLaboratorio().setNoContacto(this.txtContacto.getText());
        cp.getLaboratorio().setNuTelContacto(this.txtTelefContacto.getText());
        cp.getLaboratorio().setNuMovilContacto(this.txtMovilContacto.getText());
        cp.getLaboratorio().setNoEmailContacto(this.txtEmailContacto.getText());
        cp.getLaboratorio().setInLabInventario("N");

        if (this.cmbLabPropio.getSelectedIndex()==1){
            cp.getLaboratorio().setInLabPropio("S");
        }else if(this.cmbLabPropio.getSelectedIndex()==2){
            cp.getLaboratorio().setInLabPropio("N");
        }else{
            cp.getLaboratorio().setInLabPropio(null);
        }
        if (chbEstado.isSelected()){
            cp.getLaboratorio().setEsLaboratorio("A");
        }else{
            cp.getLaboratorio().setEsLaboratorio("I");
        }
        
        Boolean EstadoGuardar=false;
            
        if(!esActualizacion){
            cp.getLaboratorio().setIdCreaLaboratorio(AtuxVariables.vIdUsuario);
            cp.getLaboratorio().setFeCreaLaboratorio(FormatoFecha(AtuxDBUtility.getFechaHoraBD(1)));
            cp.getLaboratorio().setCoLaboratorio(cp.getNuevoCodigoLaboratorio());
            txtCodigo.setText(cp.getLaboratorio().getCoLaboratorio());
            EstadoGuardar = cp.guardarRegistro(cp.getLaboratorio());
        }else{
            cp.getLaboratorio().setIdModLaboratorio(AtuxVariables.vIdUsuario);
            cp.getLaboratorio().setFeModLaboratorio(FormatoFecha(AtuxDBUtility.getFechaHoraBD(1)));
            int act = cp.actualizarRegistro(cp.getLaboratorio());
            if (act ==1) {
                EstadoGuardar = true;
            }
        }
        
        if(EstadoGuardar){
            EstadoGuardar = true; 
        }else{
            EstadoGuardar = false; 
        }
        return EstadoGuardar;
    }

    private Date FormatoFecha(String oldFecha){
        Date Fecha=null;
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        String dateInString = oldFecha;

        try {
            Fecha = formatter.parse(dateInString);
        } catch (ParseException ex) {
            Logger.getLogger(ILaboratorio.class.getName()).log(Level.SEVERE, null, ex);
        }
        return Fecha;
    }

    
