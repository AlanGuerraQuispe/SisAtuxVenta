private void btnCerrarCajaTurnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCerrarCajaTurnoActionPerformed
    String MovCaja = null;
    CCajaPago movimientoCaja = new CCajaPago();
    try {
        String Fecha=AtuxUtility.getDateToString(AtuxUtility.getStringToDate(txtFecha.getText(), "dd/MM/yyyy"), "ddMMyyyy");
        MovCaja = movimientoCaja.getRealizaMovimientoCaja("C", Fecha, AtuxVariables.vNuSecUsuario, Integer.parseInt(txtCaja.getText()), Integer.parseInt(txtTurno.getText()));
        if (MovCaja.length()!=8){
            JOptionPane.showMessageDialog(this, MovCaja);
            return;
        }
    } catch (SQLException ex) {
        Logger.getLogger(ICierreCajaTurno.class.getName()).log(Level.SEVERE, null, ex);
    }

    if (JOptionPane.showConfirmDialog (null, "Desea imprimir la wincha de Cierre", AtuxVariables.vNombrePC, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
        String Resultado = movimientoCaja.ImpresionWincha(MovCaja);
        JOptionPane.showMessageDialog(this, Resultado);
    }    
}//GEN-LAST:event_btnCerrarCajaTurnoActionPerformed

