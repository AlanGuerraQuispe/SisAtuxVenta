    private void ValidaDatos() throws SQLException {
        AtuxUtility.setCajaTurno();
        if ("".equals(AtuxVariables.vNuCaja)){
            JOptionPane.showMessageDialog(this, "No se pudo determinar una Caja relacionada al Usuario. Verifique !!!", "Mensaje del Sistema", JOptionPane.WARNING_MESSAGE);
        }
        if ("N".equals(AtuxVariables.vInCajaAbierta)){
            JOptionPane.showMessageDialog(this, "La Caja " + AtuxVariables.vNuCaja + " NO se encuentra ABIERTA. Verifique !!!", "Mensaje del Sistema", JOptionPane.WARNING_MESSAGE);
            Ejecutar= false;
            
        }
        txtCaja.setText(AtuxVariables.vNuCaja);
        txtTurno.setText(AtuxVariables.vNuTurno);
        txtCajero.setText(AtuxVariables.vNoUsuario);
        txtFecha.setText(AtuxDBUtility.getValueAt("VTTR_CAJA_PAGO", "TO_CHAR(FE_MOD_CAJA,'DD/MM/YYYY')",
                                                  "co_compania = '" + AtuxVariables.vCodigoCompania + "' " +
                                                  "AND co_local = '" + AtuxVariables.vCodigoLocal + "' " +
                                                  "AND nu_caja = " + AtuxVariables.vNuCaja ));
    }
    
