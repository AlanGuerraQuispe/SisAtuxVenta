private void guardar() throws SQLException{
    CObsCaja ObservacionCaja = new CObsCaja();
    String Data01 = ObservacionCaja.EliminaTodasObservaciones(AtuxVariables.vCodigoCompania, 
                                               AtuxVariables.vCodigoLocal, 
                                               txtFecha.getText(), 
                                               Integer.parseInt(txtCaja.getText()), 
                                               Integer.parseInt(txtTurno.getText()));
    
    if ("NoOk".equals(Data01)){
        JOptionPane.showMessageDialog(this, "Problemas al guardar Observaciones\n Favor Verifique");
        return;
    }

    obs = new ObsCaja();
    Date Fecha=FormatoFecha(txtFecha.getText());

    ModeloTablaObsCierreCaja caja = (ModeloTablaObsCierreCaja)tblListado.getModel();

    controllerObsCaja  = new CObsCaja();

    for (int j = 0; j<tblListado.getRowCount();  j++){
        obs =(ObsCaja)caja.getFila(j);

        obs.setcoCompania(AtuxVariables.vCodigoCompania);
        obs.setcoLocal(AtuxVariables.vCodigoLocal);
        obs.setfeDiaVenta(Fecha);
        obs.setnuCaja(Integer.parseInt(txtCaja.getText()));
        obs.setnuTurno(Integer.parseInt(txtTurno.getText()));
        obs.setnuSecObs(j + 1);
        obs.setidCreaObs(AtuxVariables.vIdUsuario );
        obs.setfeCreaObs(AtuxSearch.getFechaHora());
        boolean gr = controllerObsCaja.guardarRegistro(obs);    
    }
}

private Date FormatoFecha(String oldFecha){
    Date Fecha=null;
    SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
    String dateInString = oldFecha;

    try {
        Fecha = formatter.parse(dateInString);
    } catch (ParseException ex) {
        Logger.getLogger(IObsCaja.class.getName()).log(Level.SEVERE, null, ex);
    }
    return Fecha;
}

private void LeerObs(){
    CObsCaja ObservacionCaja = new CObsCaja();
    ArrayList Data01 = ObservacionCaja.LeerObservaciones(AtuxVariables.vCodigoCompania, 
                                               AtuxVariables.vCodigoLocal, 
                                               txtFecha.getText(), 
                                               Integer.parseInt(txtCaja.getText()), 
                                               Integer.parseInt(txtTurno.getText()));

    this.mtped = new ModeloTablaObsCierreCaja(Data01);
    tblListado.setModel(mtped);
    Helper.ajustarSoloAnchoColumnas(tblListado,ModeloTablaObsCierreCaja.anchoColumnas);   
    AtuxUtility.setearPrimerRegistro(tblListado, null, 0);
    AtuxUtility.moveFocus(txtObs);
}  

private void Limpiar(){
    txtCodCajero.setText("");
    txtNombreCajero.setText("");
    txtCaja.setText("");
    txtFecha.setText("");
    txtTurno.setText("");
    txtObs.setText("");
    for (int i = tblListado.getRowCount() -1; i >= 0; i--)     {   
        ((DefaultTableModel) tblListado.getModel()).removeRow(i);
    }    
}

