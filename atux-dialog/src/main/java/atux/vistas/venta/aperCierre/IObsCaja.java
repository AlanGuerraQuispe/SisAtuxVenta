private void BotonGuardar(){
    if (JOptionPane.showConfirmDialog(this, "Esta seguro de Guardar los Datos", titulo, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION) {
        return;
    }

    try {
        guardar();
        AtuxUtility.aceptarTransaccion();
        CambioRealizado = false;
        JOptionPane.showMessageDialog(this, "Información Guardar satisfactoriamente");
    } catch (SQLException ex) {
        Logger.getLogger(IObsCaja.class.getName()).log(Level.SEVERE, null, ex);
    }
}
