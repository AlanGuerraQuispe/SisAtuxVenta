private void Agregar(){
    if ("".equals(txtObs.getText().trim())){
        JOptionPane.showMessageDialog(this, "Debe de ingresar una observación");
        return;
    }
    CambioRealizado = true;
    obs = new ObsCaja();
    Date Fecha=FormatoFecha(txtFecha.getText());

    obs.setcoCompania(AtuxVariables.vCodigoCompania);
    obs.setcoLocal(AtuxVariables.vCodigoLocal);
    obs.setfeDiaVenta(Fecha);
    obs.setnuCaja(Integer.parseInt(txtCaja.getText()));
    obs.setnuTurno(Integer.parseInt(txtTurno.getText()));
    obs.setnuSecObs(tblListado.getRowCount() + 1);
    obs.setdeObs(txtObs.getText());

    mtped.agregarFila(obs);
    tblListado.repaint();
    mtped.fireTableDataChanged();
}

