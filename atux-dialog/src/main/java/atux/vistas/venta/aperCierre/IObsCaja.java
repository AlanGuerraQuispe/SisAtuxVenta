private void EliminarRegistro(){
    if (JOptionPane.showConfirmDialog(this, "Esta seguro de Eliminar el Registro", titulo, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION) {
        return;
    }

    obs = new ObsCaja();
    int Fila = tblListado.getSelectedRow();
    if (Fila==-1){
        JOptionPane.showMessageDialog(this, "Debe de seleccionar una fila");
        return;
    }
    CambioRealizado = true;
    mtped.quitarFila(Fila);
    tblListado.repaint();
    mtped.fireTableDataChanged();
}
        
