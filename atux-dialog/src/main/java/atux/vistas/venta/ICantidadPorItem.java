    void closeWindow(boolean pAceptar) {        
      AtuxVariables.vAceptar = pAceptar;
      if (!pAceptar) txtCantidad.setText("");
          this.setVisible(false);
      this.dispose();
    }

    private void aceptarCantidad() {
        if ((txtCantidad.getText().trim().length() <= 0) || (txtCantidad.getText().trim().length() == 0)) {
                JOptionPane.showMessageDialog(this, "Por favor, ingrese una Cantidad valida !!!", "Mensaje del Sistema", JOptionPane.WARNING_MESSAGE);
                AtuxUtility.moveFocus(txtCantidad);
            }
          else           
           closeWindow(true);
    }
   
